class State:
	
	def __init__(self, x = 0.0, y = 0.0, id = " "):
		self.x = x
		self.y = y
		self.id = y
	def getX(self):
		return self.x
	def getY(self):
		return self.y
	def getId(self):
		return self.id
	def setX(self, x):
		self.x = x
	def setY(self, y):
		self.y = y
	def setId(self, id):
		self.id = id


	
	
	
